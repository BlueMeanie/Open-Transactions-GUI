#ifndef __OTASSETCONTRACT_H__
#define __OTASSETCONTRACT_H__


#include <map>
#include <string>


#include "OTContract.h"

class OTBasket;
class OTPseudonym;
class OTString;

class OTAssetContract : public OTContract 
{
protected:
	OTString	m_strBasketInfo;	// If this contract is for a basket currency, the OTBasket object is stored here.
	
	OTString m_strIssueCompany;
	OTString m_strIssueEmail;
	OTString m_strIssueContractURL;
	OTString m_strIssueType;
	
	OTString m_strCurrencyName;
	OTString m_strCurrencyTLA;
	OTString m_strCurrencySymbol;
	OTString m_strCurrencyType;
	OTString m_strCurrencyFactor;
	OTString m_strCurrencyDecimalPower;
	OTString m_strCurrencyFraction;
	
	// return -1 if error, 0 if nothing, and 1 if the node was processed.
	virtual int ProcessXMLNode(irr::io::IrrXMLReader*& xml);
public:
	bool CreateBasket(OTBasket & theBasket, OTPseudonym & theSigner);

	inline const OTString & GetBasketInfo() const { return m_strBasketInfo; }
	
	OTAssetContract();
	OTAssetContract(OTString & name, OTString & filename, OTString & strID);
	virtual ~OTAssetContract();
	
	virtual bool CreateContract(OTString & strContract, OTPseudonym & theSigner);
	
	virtual bool SaveContractWallet(FILE * fl);
};


typedef std::map<std::string, OTAssetContract *>	mapOfContracts;



#endif // __OTASSETCONTRACT_H__
